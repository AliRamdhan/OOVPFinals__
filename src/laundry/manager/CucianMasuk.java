    private int calculatePrice(int weight) {
        int pricePerKg = 5000;
        return weight * pricePerKg;
    }

    private boolean deleteCucianById(int cucianId) {
        String deleteCucianQuery = "DELETE FROM cucian WHERE id_cucian=?";
        try ( PreparedStatement statement = conn.prepareStatement(deleteCucianQuery)) {
            statement.setInt(1, cucianId);
            statement.executeUpdate();
            return true;
        } catch (SQLException e) {
            e.printStackTrace();
            return false;
        }
    }

