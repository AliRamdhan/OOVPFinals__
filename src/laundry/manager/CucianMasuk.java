    private void tampil() {
        try {
            model.setRowCount(0); // clear the table
            String query = "SELECT * FROM cucian JOIN pelanggan ON cucian.id_pelanggan = pelanggan.id_pelanggan";
            Statement statement = conn.createStatement();
            ResultSet result = statement.executeQuery(query);
            if (!result.next()) {
                JOptionPane.showMessageDialog(this, "No data found!", "Notification", JOptionPane.WARNING_MESSAGE);
            } else {
                do {
                    Object[] row = {
                        result.getString("id_pelanggan"),
                        result.getString("id_cucian"),
                        result.getString("nama_pelanggan"),
                        result.getString("nomer_hp"),
                        result.getString("alamat_pelanggan"),
                        result.getString("jenis_kelamin"),
                        result.getInt("berat"),
                        result.getString("tanggal_masuk"),
                        result.getInt("harga"),
                        result.getString("statuspembayaran"),
                        result.getString("statustransaksi"),
                        result.getString("tanggal_keluar")
                    };
                    model.addRow(row);
                } while (result.next());
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

